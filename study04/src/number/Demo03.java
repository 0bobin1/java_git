package number;

public class Demo03 {
    public static void main(String[] args) {
        double a = -1.5;
        System.out.println(Math.floor(a));//向下取整
        System.out.println(Math.round(a));//四舍五入   (x+0.5)再向下取整
        System.out.println(Math.ceil(a));//向上取整
        System.out.println("66\t65\t55");
    }
}
